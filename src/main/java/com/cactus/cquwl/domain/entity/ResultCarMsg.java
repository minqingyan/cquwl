package com.cactus.cquwl.domain.entity;

public class ResultCarMsg {
	
	private int id;
	private String carNumber;
	private String carMsg;
	private String phone;
	private String currentPlace;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the carNumber
	 */
	public String getCarNumber() {
		return carNumber;
	}
	/**
	 * @param carNumber the carNumber to set
	 */
	public void setCarNumber(String carNumber) {
		this.carNumber = carNumber;
	}
	/**
	 * @return the carMsg
	 */
	public String getCarMsg() {
		return carMsg;
	}
	/**
	 * @param carMsg the carMsg to set
	 */
	public void setCarMsg(String carMsg) {
		this.carMsg = carMsg;
	}
	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}
	/**
	 * @param phone the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}
	/**
	 * @return the currentPlace
	 */
	public String getCurrentPlace() {
		return currentPlace;
	}
	/**
	 * @param currentPlace the currentPlace to set
	 */
	public void setCurrentPlace(String currentPlace) {
		this.currentPlace = currentPlace;
	}
	
}
