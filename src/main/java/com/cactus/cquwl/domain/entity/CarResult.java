package com.cactus.cquwl.domain.entity;

public class CarResult {

	private Integer id;
	private String carNumber;
	private String carMsg;
	private String carToPlace;
	private String carNowPlace;
	private String carPhone;
	
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * @return the carNumber
	 */
	public String getCarNumber() {
		return carNumber;
	}
	/**
	 * @param carNumber the carNumber to set
	 */
	public void setCarNumber(String carNumber) {
		this.carNumber = carNumber;
	}
	/**
	 * @return the carMsg
	 */
	public String getCarMsg() {
		return carMsg;
	}
	/**
	 * @param carMsg the carMsg to set
	 */
	public void setCarMsg(String carMsg) {
		this.carMsg = carMsg;
	}
	
	/**
	 * @return the carToPlace
	 */
	public String getCarToPlace() {
		return carToPlace;
	}
	/**
	 * @param carToPlace the carToPlace to set
	 */
	public void setCarToPlace(String carToPlace) {
		this.carToPlace = carToPlace;
	}
	/**
	 * @return the carNowPlace
	 */
	public String getCarNowPlace() {
		return carNowPlace;
	}
	/**
	 * @param carNowPlace the carNowPlace to set
	 */
	public void setCarNowPlace(String carNowPlace) {
		this.carNowPlace = carNowPlace;
	}
	/**
	 * @return the carPhone
	 */
	public String getCarPhone() {
		return carPhone;
	}
	/**
	 * @param carPhone the carPhone to set
	 */
	public void setCarPhone(String carPhone) {
		this.carPhone = carPhone;
	}
	
}
